		<h2>
		Bröllop - Vanliga frågor &amp; svar
		</h2>
		<p class="question">Hur bokar vi en bröllopsfotografering?</p>
		<p class="answer">
			Kontakta mig via formuläret på kontaktsidan eller ring så bokar vi in ett personligt möte.
		</p>
		<p class="question">Personligt möte?</p>
		<p class="answer">
			Under vårt personliga möte går vi igenom vad ni har för önskemål och vad ni förväntar er av fotograferingen. Jag visar bilder jag tagit tidigare samt exempel på album.
			<br /><br />
			Det är viktigt att dagen flyter på. Vi bestämmer detaljer runt bröllopsdagen, som t ex. förslag på plats, om ni vill fotograferas innan/efter vigsel, under vigsel, mingel efter vigsel, grupp-porträtt tillsammans med familj eller tärnor samt om ni vill att jag skall fotografera under festen senare på dagen.
		</p>
		<p class="question">Vad händer sedan?</p>
		<p class="answer">
			Jag lägger stor vikt vid förberedelser och är alltid på plats en tid innan bröllopet för att "reka". Jag tittar ut hur jag på ett praktiskt sätt kan lägga upp fotograferingen så att det går så smidigt som möjligt när tiden är inne.
		</p>
		<p class="question">Hur går fotograferingen till?</p>
		<p class="answer">
			Vi träffas i god tid på den plats vi valt ut tidigare. Räkna med 2 timmar effektiv fotografering samt extra tid för eventuell transport.
		</p>
		<p class="question">Varför skall vi betala för en professionell fotograf, när en vän till oss har en grym kamera och har sagt att han kan ställa upp.</p>
		<p class="answer">
			En professionell fotograf är förberedd på att vad som helst kan hända. Går kameran sönder så har man en reservkamera.

Det är väldigt vanligt att man tror att om man har en bra kamera så tar man automatiskt bra bilder, men en bra bild hänger inte på utrustningen.

Alla professionella fotografer vet att för en bra bild behövs bra ljus. Som professionell har man även utrustning för att få bra ljus även när det naturliga ljuset inte är på plats.


		</p>
		<p class="question">Får vi en skiva med original bilder som vi själva kan redigera göra förstoringar på?</p>
		<p class="answer">
Nej. De bilder jag tar levereras färdig-redigerade, på två sätt.

På skiva i lågupplöst format vars bilder kan användas på t ex. Facebook eller en bröllopets hemsida.

Printade bilder. Antingen förstoringar eller bilder i album etc.
<br /><br />
Bilderna får ej redigeras utan min inblandning. Vill ni ha svartvita kopior så skall ni höra av er till mig.
<br /><br />
Bilderna får ej kopieras i en scanner och förstoras.

		</p>

		<p class="question">Varför kan vi inte få högupplösta bilder?</p>
		<p class="answer">
			Som fotograf vill man ha kontroll på att de bilder som slutligen hänger på väggen håller högsta kvalité och färgåtergivning.

Om jag skulle ge bort högupplösta original så skulle det innebära att jag på inget sätt kan kontrollera slutresultatet.</p>

		<p class="question">Vem äger rättigheterna till bilderna?</p>
		<p class="answer">
			Fotografen äger alltid fulla rätten till bilderna och kan användas fritt av fotografen i marknadsföringssyfte om ej annan överenskommelse finns.

De bilder ni som kund får på skiva får användas för privat bruk.


		</p>

