		<h2>
		Porträtt - Vanliga frågor &amp; svar
		</h2>

		<p class="question">Hur bokar jag en porträttfotografering?</p>
		<p class="answer">Kontakta mig via formuläret på kontaktsidan eller ring så bokar vi en tid i studion samt diskuterar igenom de idéer ni har. Ni är givetvis välkomna att ringa igen om ni har fler frågor.</p>

		<p class="question">Behöver vi tänka på något inför fotograferingen?</p>
		<p class="answer">Gäller det babyfoto så är det bra om vi bokar en tid som passar ert barn, gärna under barnets "gladaste" stund på dagen. Det är bra om ni tar med er några av barnets favoritleksaker, eller annan rekvisita som ni tycker kan passa in.</p>

		<p class="question">Hur går fotograferingen till?</p>
		<p class="answer">När ni kommer till studion så är jag redo att börja fotografera. Jag förbereder allting innan ni kommer så att fokus ligger på det viktiga när ni är på plats. Vi kör på och är kreativa under ungefär en timma eller tills vi känner att vi har det material vi behöver.</p>

		<p class="question">Vad händer efter fotograferingen?</p>
		<p class="answer">Det första jag gör efter fotograferingen är att arkivera bilderna samt göra en backup för att säkra bilderna. 

Jag går sedan noggrant igenom bilderna och väljer ut de bästa.

Dessa bilder gör jag sedan grundläggande bilbehandling på och lägger upp på en temporär webbsida som ni som kund får tillgång till.

På denna sida plockar ni ut de bilder ni vill ha förstoringar på genom att notera bildnumret. För att beställa skickar ni sedan ett mail till mig med bildnummer samt vilken storlek på förstoring och antal.

</p>

		<p class="question">Hur lång tid tar det innan vi får förstoringarna?</p>
		<p class="answer">Räkna med ungefär en vecka för att förbereda bilderna och sedan ytterligare en vecka hos labbet där bilden skall skrivas ut, monteras och lamineras. När bilderna sedan levereras till mig så paketeras de och skickas de till kund.</p>

		<p class="question">Kan vi beställa fler bilder senare?</p>
		<p class="answer">Ja. Bilderna arkiveras hos mig. Om ni senare ångrar att ni inte tog en förstoring av "den där andra" bilden så är ni välkomna att kontakta mig.</p>


