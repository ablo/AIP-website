		<h4>Ett minne för livet</h4>
		<h2>Album</h2>
		<p>
		Mina album är av läderkartong och är tillverkade i sverige. 
		Inlagor monteras i efterhand vilket gör att det finns stor valmöjlighet när det gäller att anpassa ert album. 
		Texten på albumet kan ändras efter behov.
		Se ett exempel på ett anpassat album <a href="gallery/timthumb.php?src=gallery/_ALBUM/_MG_8787.jpg&w=800&h=&amp;zc=1&amp;q=100" class="information_image" title="Exempel på bröllopsalbum med egen text på framsidan">här</a>.
		</p>
		<p>Albumet finns i storleken 18x20.</p>
		<h2>Förstoringar</h2>
		<p>Förstoringar beställs direkt av mig och är av absolut högsta kvalité. 
		Förstoringar på 24x30 och uppåt är laminerade och monterade på syrafri kartong för lång 
		hållbarhet.</p>
		<p>Jag erbjuder förstoringar ända upp till 70x100 cm. Finns även möjlighet att beställa canvas. <a href="/index.php?page_id=45">Kontakta mig</a> för mer info och priser.</p>
